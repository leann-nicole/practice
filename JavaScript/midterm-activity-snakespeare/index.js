let storiesDisplayed = 0

function displayTiles(){
    let storyCols = document.querySelectorAll(".storyCol")
    for (let col = 0; col < 3; col++){
        for (let y = 1; y <= 3; y++){
            let story = document.createElement("div")
            story.className = "story"
            story.innerHTML = poems[storiesDisplayed]
            story.addEventListener("click",displayStory)
            storyCols[col].appendChild(story)
            storiesDisplayed++
            if (storiesDisplayed == poems.length) break
        }
        if (storiesDisplayed == poems.length) break
    }
}

displayTiles()

let upButton= document.getElementById("returnButton")
let rootElement = document.documentElement

upButton.addEventListener("click", scrollToTop)

function scrollToTop() {
    rootElement.scrollTo({
        top: 0,
        behavior: "smooth"
    })
}

let panel = document.getElementById("selectedStory")
let panelDisplayed = false

function displayStory(){
    if (panelDisplayed){
        hidePanel()
        panelDisplayed = false
    } 
    else{
        panel.setAttribute("style", "display: block;")
        panel.innerHTML = this.innerHTML
        panelDisplayed = true
    }
}

function hidePanel(){
    panel.setAttribute("style", "display: none;")
    panelDisplayed = false
}